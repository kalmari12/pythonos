szam1 = int(input("Kérem az első számot: "))
szam2 = int(input("Kérem a második számot: "))

ossz = szam1 + szam2
kiv = szam1 - szam2
szorz = szam1 * szam2
hanyad = szam1 / szam2
telje = ossz + kiv + szorz + hanyad

print(f"\t<< Eredmények >>\n\n"
      f"\tA két szám összege: {ossz}\n"
      f"\tA két szám különbsége: {kiv}\n"
      f"\tA két szám szorzata: {szorz}\n"
      f"\tA két szám hányadosa: {hanyad}\n"
      f"\tAz összes műveletnek az egybevett összege: {telje}\n\n"
      f"\t<< Vége >>"  )












